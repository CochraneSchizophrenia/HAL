    private void btn_open2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_open2MouseClicked
        // JFileChooser-Objekt erstellen
        try{
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter( "CSV files",  "csv");
            chooser.setFileFilter(filter);

            int rueckgabeWert = chooser.showOpenDialog(null);

            /* Abfrage, ob auf "Öffnen" geklickt wurde */
            if(rueckgabeWert == JFileChooser.APPROVE_OPTION)
            {
                // Ausgabe der ausgewaehlten Datei
                text2.setText(chooser.getSelectedFile().getAbsolutePath());

                if (!text2.getText().substring(text2.getText().indexOf(".")).equals(".csv")){
                    JOptionPane.showMessageDialog(this, "Please choose a valid .csv file", "Warning", JOptionPane.WARNING_MESSAGE);
                    text2.setText("");
                }
                else {
                    FileReader fr = new FileReader(text2.getText());
                    BufferedReader br = new BufferedReader(fr);
                    String zeile = br.readLine();
                    String [] arr = zeile.split(";");
                    if (arr.length != 46) {
                        JOptionPane.showMessageDialog(this, "Chosen file has not the suitable structure: number of colums are not matching. Please export it again or choose another file.", "Error", JOptionPane.ERROR_MESSAGE);
                        text2.setText("");
                    }
                    br.close();
                    fr.close();
                }
            }
        }
        catch (Exception e) {

        }
    }//GEN-LAST:event_btn_open2MouseClicked

